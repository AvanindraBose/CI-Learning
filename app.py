def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


if __name__ == "__main__":
    result1 = add(5, 3)
    result2 = subtract(5, 3)
    result3 = multiply(5, 3)

    print("Addition:", result1)
    print("Subtraction:", result2)
    print("Multiplication:", result3)
